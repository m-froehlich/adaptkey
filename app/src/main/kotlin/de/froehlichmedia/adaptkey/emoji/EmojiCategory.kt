package de.froehlichmedia.adaptkey.emoji

/**
 * The emoji panel's fixed category set (L-03). [icon] is the representative glyph shown on the
 * category's tab.
 */
enum class EmojiCategory(val icon: String) {
    SMILEYS_PEOPLE("😀"),
    ANIMALS_NATURE("🐻"),
    FOOD_DRINK("🍔"),
    ACTIVITIES("⚽"),
    TRAVEL_PLACES("✈"),
    OBJECTS("💡"),
    SYMBOLS("❤")
}
